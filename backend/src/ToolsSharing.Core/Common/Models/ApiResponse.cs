namespace ToolsSharing.Core.Common.Models;

public class ApiResponse<T>
{
    public bool Success { get; set; }
    public string? Message { get; set; }
    public T? Data { get; set; }
    public List<string> Errors { get; set; } = new();

    public static ApiResponse<T> SuccessResult(T data, string? message = null)
    {
        return new ApiResponse<T>
        {
            Success = true,
            Data = data,
            Message = message
        };
    }

    public static ApiResponse<T> ErrorResult(string message, List<string>? errors = null)
    {
        return new ApiResponse<T>
        {
            Success = false,
            Message = message,
            Errors = errors ?? new List<string>()
        };
    }

    // Convenience methods with different names
    public static ApiResponse<T> CreateSuccess(T data, string? message = null) => SuccessResult(data, message);
    public static ApiResponse<T> CreateFailure(string message, List<string>? errors = null) => ErrorResult(message, errors);
}